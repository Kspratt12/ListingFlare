import type { Metadata } from "next";
import { Inter, Playfair_Display } from "next/font/google";
import "./globals.css";

const inter = Inter({
  subsets: ["latin"],
  variable: "--font-inter",
  display: "swap",
});

const playfair = Playfair_Display({
  subsets: ["latin"],
  variable: "--font-playfair",
  display: "swap",
});

// Force all pages to render dynamically (no static pre-rendering)
// Required because most pages depend on Supabase auth/cookies at runtime
export const dynamic = "force-dynamic";

export const metadata: Metadata = {
  title: "ListingFlare — Stunning Property Websites",
  description:
    "Beautiful single-property listing websites for luxury real estate agents.",
};

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html lang="en" className={`${inter.variable} ${playfair.variable}`}>
      <body className="font-sans antialiased">{children}</body>
    </html>
  );
}
